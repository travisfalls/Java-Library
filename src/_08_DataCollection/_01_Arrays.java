package _08_DataCollection;

import java.util.Arrays;

public class _01_Arrays {

	public static void main(String[] args) {

		
		//My Example
		String[] strArray = {"Travis", "Kady", "Haddie"};
		
		System.out.println(Arrays.toString(strArray));
		
		System.out.println(strArray[1]);
		
		
		int[] intArray = {3, 6, 9, 12, 15};
		
		System.out.println(Arrays.toString(intArray));
		
		
		double[] doubleArray = {3.2, 5.7, 9.11, 6.26};
		
		System.out.println(Arrays.toString(doubleArray));
		
		
		//Class Example
		
		String[] names = {"Paul", "Sydney", "John", "Amanda"};
		
		System.out.println(names[0]);
		
		
		//Class Practice
		
		String[] favFoods = {"Pizza", "Tacos", "Fries", "Ice Cream"};
		
		System.out.println(favFoods[0]);
		System.out.println(favFoods[1]);
		System.out.println(favFoods[2]);
		System.out.println(favFoods[3]);
		
		int[] familyAges = {31, 29, 2};
		
		System.out.println(familyAges[1]);
		
		
		
		

	}

}

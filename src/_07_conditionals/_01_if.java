package _07_conditionals;

public class _01_if {

	public static void main(String[] args) {
		
		String favFood = "tacos";
		
		if (favFood.equals("pizza")){
			System.out.println("I love pizza!");
		}
		
		if (favFood.equals("hamburgers")){
			System.out.println("Good thing it's hamburger day for lunch.");
		}
		
		if (favFood.equals("tacos")){
			System.out.println("Tacos are my favorite as well!");
		}
		
		
		
		
	}

}
